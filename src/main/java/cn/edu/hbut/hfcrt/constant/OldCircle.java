package cn.edu.hbut.hfcrt.constant;

public class OldCircle {
	public String CircleSortResult;
	public String CircleLeftConvergence;
	public String CircleLeftConvergenceId;
	public String CircleRightConvergence;
	public String CircleRightConvergenceId;
	public String getCircleSortResult() {
		return CircleSortResult;
	}
	public void setCircleSortResult(String circleSortResult) {
		CircleSortResult = circleSortResult;
	}
	public String getCircleLeftConvergence() {
		return CircleLeftConvergence;
	}
	public void setCircleLeftConvergence(String circleLeftConvergence) {
		CircleLeftConvergence = circleLeftConvergence;
	}
	public String getCircleLeftConvergenceId() {
		return CircleLeftConvergenceId;
	}
	public void setCircleLeftConvergenceId(String circleLeftConvergenceId) {
		CircleLeftConvergenceId = circleLeftConvergenceId;
	}
	public String getCircleRightConvergence() {
		return CircleRightConvergence;
	}
	public void setCircleRightConvergence(String circleRightConvergence) {
		CircleRightConvergence = circleRightConvergence;
	}
	public String getCircleRightConvergenceId() {
		return CircleRightConvergenceId;
	}
	public void setCircleRightConvergenceId(String circleRightConvergenceId) {
		CircleRightConvergenceId = circleRightConvergenceId;
	}
	@Override
	public String toString() {
		return "OldCircle [CircleSortResult=" + CircleSortResult + ", CircleLeftConvergence=" + CircleLeftConvergence
				+ ", CircleLeftConvergenceId=" + CircleLeftConvergenceId + ", CircleRightConvergence="
				+ CircleRightConvergence + ", CircleRightConvergenceId=" + CircleRightConvergenceId + "]";
	}
	
	

}
